package com.example.recyclerview.Activities;

public class Model2 {
    private int story;
    private String snm;

    public Model2(int story, String snm) {
        this.story = story;
        this.snm = snm;
    }

    public int getStory() {
        return story;
    }

    public void setStory(int story) {
        this.story = story;
    }

    public String getSnm() {
        return snm;
    }

    public void setSnm(String snm) {
        this.snm = snm;
    }
}
